for(var num = 0; num<16; num++){
    if(num%2==0){
        document.write(num.toString() + " is even" + "<br>");
        window.console.log(num.toString() + " is even");
    }else{ 
        document.write(num.toString() + " is odd" + "<br>");
        window.console.log(num.toString() + " is odd");
    }
}